package com.example.order_101.Model;

public class Restaurants {
    private  String name, image;

    public Restaurants() {
    }

    public Restaurants(String name, String image) {
        this.name = name;
        this.image = image;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getImage() {
        return image;
    }

    public void setImage(String image) {
        this.image = image;
    }
}
